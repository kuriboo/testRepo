この画像は、水道料金システムのシステム設定画面を示しています。画面上には、業務メッセージ、請求先情報、シミュレーションパス、料金改定ファイル名、料金改定日の設定項目があります。

業務メッセージの項目では、業務コードと内容を設定できます。シミュレーションパスでは、様々なパスを指定可能です。料金改定ファイル名では、hotel、ryokin、keijyoなどのファイルを指定でき、料金改定日も入力できるようになっています。

年次更新の不納欠損バスと受水槽洗浄データバスのパスも設定項目に含まれています。

全体としてシステムの各種パスやファイル、日付などを一括設定できる、水道料金システムの重要な設定画面だと言えます。